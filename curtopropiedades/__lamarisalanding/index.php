		$(document).ready(function(){

			$('#formBudget').validator().submit(function(e){
				if (e.isDefaultPrevented()) {
					$('#formBudgetResponse').html('<i class="fal fa-times fa-fw"></i> Datos requeridos incompletos (*)');    
				} else {
					$.ajax({
						type: "POST",
						url: 'process/budget.php',
						data: $(this).serialize(),
						success: function(response){
							console.log(response);
							switch (response) {
								case 'success':
								$('#formBudget')[0].reset();
								$('#formBudgetResponse').html('<i class="fal fa-check fa-fw"></i> Mensaje enviado con éxito!');
								break;
								case 'error':
								$('#formBudgetResponse').html('<i class="fal fa-times fa-fw"></i> Existen errores en la transacción.');
								break;
								default:
								$('#formBudgetResponse').html('<i class="fal fa-times fa-fw"></i> '+response);
								break;
							}
						}
					});
				}
				return false;
			});

		});

